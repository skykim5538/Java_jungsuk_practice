package ch6.Ex6_6;

public class MyMath {
    long add(long a, long b) {
        long result = a+b;
        return result;

    }

    long sub(long a, long b) {
//        long result = a-b;
        return a-b;
    }
    long mul(long a, long b) {
        return a*b;
    }
    long div(long a, long b) {
        return a/b;
    }
    double div(double a, double b) {
        return a/b;
    }

}
